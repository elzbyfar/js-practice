function areFollowingPatterns(strings, patterns) {
	let pairing = {};

	for (let i = 0; i < patterns.length; i++) {
		// if (strings[i] === 'd') {
		// 	console.log(patterns[i]);
		// } else if (patterns[i] === 'd') {
		// 	console.log(strings[i]);
		// }

		if (pairing[strings[i]] || pairing[patterns[i]]) {
			if (patterns[i] !== pairing[strings[i]] || strings[i] !== pairing[patterns[i]]) {
				return false;
			}
		} else {
			pairing[strings[i]] = patterns[i];
			pairing[patterns[i]] = strings[i];
		}
	}

	return true;
}

